#Goal: Define a function that takes a
#       number and returns one more
#       than that number

def one_more(num):
    #Here, we're inside our
    #function definition
    answer = num - 1
    return answer



#We've define our function
#Now we need to call it!
my_num = 5
plus_one = one_more(my_num) #call
print(my_num)
print(plus_one)
print(one_more(99)) #call
